a = int(input('Digite um número: '))
antec = a - 1
suces = a + 1
dob = a * 2
trip = a * 3
rq = a ** 2
print('Seu antecessor é: {}, seu sucessor é: {}'.format(antec, suces))
print('O dobro desse número é: {}, seu triplo é {}, e sua raiz quadrada é {}'.format(dob, trip, rq))

b1 = int(input('Digite a nota do primeiro bimestre: '))
b2 = int(input('Digite a nota do segundo bimestres: '))
media = (b1 + b2) / 2
print('A média calculada das notas é: {}'.format(media))

alt = float(input('Qual sua altura em metros?'))
cent = alt * 100
milim = alt * 1000
print('Sua altura em centímetros é igual a {}cm'.format(cent))
print('Sua altura em milímetros equivale a {}mm'.format(milim))




